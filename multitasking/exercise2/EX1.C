/*
*********************************************************************************************************
*                                             CREATE TASKS
*********************************************************************************************************
*/

static  void  TaskStartCreateTasks (void)
{
    INT8U  i;
    for (i = 0; i < N_TASKS; i++) { /* Create N_TASKS identical tasks           */
		TaskData[i] = '0' + i;
		// 1,2,3,4번 테스크 모두 생성 (초반에는 1번 테스크가 우선순위 1, 2번 테스크가 우선순위 2, 3번 테스크가 우선순위 3, 4번 테스크가 우선순위 4를 가지도록 합니다)
		OSTaskCreate(Task, (void *)&TaskData[i], &TaskStk[i][TASK_STK_SIZE - 1], i+1);				
    }
}

/*
*********************************************************************************************************
*                                                  TASKS
*********************************************************************************************************
*/

void  Task (void *pdata)
{
    INT8U  x;
    INT8U  y;
    INT8U  err;
	INT8U  i;

    for (;;) {			
		char *data = (char *)pdata;				
		OSSemPend(PrintSem,0,&err);		
		if (*data == '0') { // Red 출력 (1번 테스크)
			for (i = 5; i < 21; i++) {
				PC_DispStr( 0,  i, "                                                                                ", DISP_FGND_WHITE + DISP_BGND_RED);
			}						
		}
		else if (*data == '1') { // Blue 출력	(2번 테스크)						
			for (i = 5; i < 21; i++) {
				PC_DispStr( 0,  i, "                                                                                ", DISP_FGND_WHITE + DISP_BGND_BLUE);
			}			
		}
		else if (*data == '2') { // Brown 출력 (3번 테스크)
			for (i = 5; i < 21; i++) {
				PC_DispStr( 0,  i, "                                                                                ", DISP_FGND_WHITE + DISP_BGND_BROWN);
			}
		}		
		else if (*data == '3') { // Green 출력 (4번 테스크)
			for (i = 5; i < 21; i++) {
				PC_DispStr( 0,  i, "                                                                                ", DISP_FGND_WHITE + DISP_BGND_GREEN);
			}					
		}				
		OSTimeDlyHMSM(0,0,2,0);	
		OSSemPost(PrintSem);
    }
}


